def generate_clean_pairs(raw_pairs: list[tuple[str, str]], max_count: int = 200) -> list[tuple[str, str]]:
    """Return only pairs where both sides have 2–40 space-delimited tokens."""
    cleaned = []
    for si, ta in raw_pairs:
        si_words = si.split()
        ta_words = ta.split()
        if 2 <= len(si_words) <= 40 and 2 <= len(ta_words) <= 40:
            cleaned.append((si, ta))
        if len(cleaned) >= max_count:
            break
    return cleaned


