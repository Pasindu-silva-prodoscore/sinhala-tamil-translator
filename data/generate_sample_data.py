    pairs = generate_clean_pairs(PAIRS, max_count=200)

    si_out = out_dir / "corpus.si"
    ta_out = out_dir / "corpus.ta"
    si_out.write_text("\n".join(si for si, _ in pairs), encoding="utf-8")
    ta_out.write_text("\n".join(ta for _, ta in pairs), encoding="utf-8")
    print(f"Wrote {len(pairs)} pairs → {si_out}, {ta_out}")


